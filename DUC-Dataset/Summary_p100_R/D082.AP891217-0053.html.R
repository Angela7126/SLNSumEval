<a href="#0" id="0">There were signs of grief throughout this snowy capital of 9 million people, as though a government leader had died.</a>
<a href="#1" id="1">President Mikhail S. Gorbachev and other Soviet leaders said in an obituary that Sakharov had been the victim of ``gross injustices'' and praised him as a ``man of honesty and sincerity.''</a>
<a href="#2" id="2">Crimean Tatars and Jewish refuseniks _ two of the groups Sakharov helped _ joined rank-and-file Communists, teachers and engineers in filing past the body of the physicist, Nobel Peace Prize winner, human rights advocate and legislator.</a>
