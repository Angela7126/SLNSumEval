<a href="#0" id="0">The government has decided to give ethnic Germans and Crimean Tatars who were expelled from their homelands by Josef Stalin their own autonomous regions but it will take years, Tass reported Wednesday.</a>
<a href="#1" id="1">The Nov. 28 plan approved by the Supreme Soviet recommended excluding from the German region areas where resistance to German resettlement was strongest in order to avoid ethnic conflicts.</a>
<a href="#2" id="2">Both sides claim control over the mountainous enclave.</a>
<a href="#3" id="3">The Tatars and Germans, among others, long have been agitating to return from Siberia and Central Asia to their traditional lands, and last month the Supreme Soviet legislature declared that all peoples exiled under Stalin should be considered politically rehabilitated.</a>
