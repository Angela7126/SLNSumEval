<a href="#3" id="3">Aug. 2: Iraqi troops invade Kuwait at 2 a.m. local time.</a>
<a href="#4" id="4">Kuwait goes on state of alert.</a>
<a href="#5" id="5">July 31: Kuwaiti, Iraqi delegations meet in Jidda, Saudi Arabia, to negotiate oil and border dispute.</a>
<a href="#6" id="6">July 24: The U.S. Defense Department announces that Navy warships and aircraft are holding a "short-notice exercise" in the Persian Gulf with the United Arab Emirates, about 600 miles southeast of the Kuwait-Iraqi border.</a>
