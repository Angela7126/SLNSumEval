<a href="#0" id="0">U.N. Agency Projects Food Shortages Due To U.S. Drought.</a>
<a href="#1" id="1">Grain production in South America should be slightly lower this year than last.</a>
<a href="#2" id="2">The high grain prices resulting from a lower supply would hit poor, food-importing countries the hardest, the U.N. agency warned.</a>
<a href="#3" id="3">The most severely hit crops are coarse grains _ mostly maize, barley and oats, the report said.</a>
<a href="#4" id="4">As a result, world grain stocks are projected to fall below 310 million tons, or 17 percent of the level the agency considers necessary to safeguard world food security, the report said.</a>
<a href="#5" id="5">If crop conditions in the United States and Canada continue to deteriorate, the ratio could drop even further.</a>
