<a href="#0" id="0">East Germany's democratic government has changed its mind several times on whether to pursue charges against the aging members of the former hard-line regime that was toppled in last autumn's pro-democracy protests.</a>
<a href="#1" id="1">The government announced in June that Honecker, Mielke and several border guards would be investigated on possible murder charges in connection with those deaths.</a>
<a href="#2" id="2">The murder charge could put Honecker behind bars for life, but his poor health makes prosecution questionable.</a>
<a href="#3" id="3">Honecker and Erich Mielke, head of East Germany's once-feared secret police agency, were interrogated by federal prosecutors, said Peter Przybylski, a Justice Ministry spokesman.</a>
