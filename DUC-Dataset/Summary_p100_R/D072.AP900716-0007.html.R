<a href="#0" id="0">Conductor Leonard Bernstein has canceled several performances in Japan due to exhaustion and lingering illness and will return to the United States to recover, his manager announced today.</a>
<a href="#1" id="1">The Japan festival is believed to be the first festival in Asia where young musicians can train with a professional orchestra.</a>
<a href="#2" id="2">On Saturday and Sunday, Bernstein canceled appearances with an orchestra of young musicians from the Pacific-Asia region who had been training under principals of the LSO during the three-week Pacific Music Festival in northern Japan.</a>
<a href="#3" id="3">Bernstein had been scheduled to conduct the London Symphony Orchestra in four performances in Japan, ending with a concert on July 22.</a>
