<a href="#0" id="0">Defense Secretary-designate John Tower contradicted an earlier sworn statement when he told the Senate his work for a British firm involved no military matters, according to reports published Saturday.</a>
<a href="#1" id="1">In written testimony to the Senate panel on Jan. 26, Tower said he could ``recall no actions in connection with any defense activities'' in connection with his work for the U.S. subsidiary of British Aerospace, Inc. That statement, according to two publications, The Los Angeles Times and the Legal Times, did not square with information which Tower provided to divorce attorneys working for his second wife in June of 1987.</a>
