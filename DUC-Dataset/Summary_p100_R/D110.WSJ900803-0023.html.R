<a href="#0" id="0">Administration and congressional officials agree that the CIA did its job.</a>
<a href="#1" id="1">WASHINGTON -U.S. intelligence officials had been sounding alarms in recent days about a possible Iraqi invasion of Kuwait.</a>
<a href="#2" id="2">Central Intelligence Agency Director William Webster even hand-delivered certain fresh details on the Iraqi buildup to National Security Adviser Brent Scowcroft.</a>
<a href="#3" id="3">The agency lacked hard evidence of an intent to invade, intelligence officials say.</a>
<a href="#4" id="4">But as the buildup grew, the CIA became more alarmed, partly because Mr. Hussein is so unpredictable, and partly because, unlike many other Arab leaders, he has been known to carry out his threats.</a>
