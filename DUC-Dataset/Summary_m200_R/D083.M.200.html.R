<a href="#0" id="0">Thunderstorms wreaked havoc across the middle Mississippi Valley and the Southeast on Wednesday with lethal tornadoes, wind up to 100 mph and hail up to the size of baseballs.</a>
<a href="#1" id="1">Winds gusted to 65 mph in Baltimore, 76 mph in Lancaster, Pa., and 100 mph at Great Valley, Pa.</a>
<a href="#2" id="2">Witnesses said the downtown Jones Valley Elementary School, the Waterford Square and adjacent Queensbury apartment complexes were reduced to rubble by the tornado.</a>
<a href="#3" id="3">Three people were injured in an earlier tornado in Clay County in eastern Alabama.</a>
<a href="#4" id="4">``It's like taking six to 10 city blocks and putting them in a blender and putting it on liquefy,'' said rescue worker Bob Caraway.</a>
<a href="#5" id="5">The storm was particularly violent because it occurred along a line of extreme variations in temperature.</a>
<a href="#6" id="6">Telephone communication and electricity were disrupted by the tornadoes and WAAY-TV, the only station still broadcasting, reported widespread damage.</a>
<a href="#7" id="7">Seven children ages 7 and 8 were killed and 18 others were injured, four critically, officials said.</a>
<a href="#8" id="8">No injuries were reported.</a>
<a href="#9" id="9">Wednesday morning's low for the Lower 48 states was 9 degrees above zero at Ely, Nev.</a>
<a href="#10" id="10">A tornado watch was posted for northeast New York state, much of Vermont and much of New Hampshire.</a>
