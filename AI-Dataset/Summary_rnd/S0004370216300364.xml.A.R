<a href="#1" id="1">Models that try to estimate the order of the opponent's preferences over different negotiation issues (the exact value or at least the order of issue weights) [14], [15], [21], [22].</a>
<a href="#2" id="2">Experimental setting</a>
<a href="#3" id="3">For each domain there could be two preference profiles (one for each side of the negotiation) which together with the domain construct a negotiation scenario.</a>
<a href="#4" id="4">The POPPONENT is evaluated in 5 separate states which include the perfect information state (PP), the constant value states (with constant values of 0.6 (P6), 0.8 (P8), 1 (P1)), and the Adaptive state (AP).</a>
<a href="#5" id="5">Each agent tries to take advantage of the other party for gaining a maximum utility for its own.</a>
<a href="#6" id="6">Key findings</a>
<a href="#7" id="7">Each one of these 20 {a mathematical formula}(=8+5+1+1+5)opponent models is equipped with a bidding strategy to compete with an opponent agent.</a>
<a href="#8" id="8">Actually, an opponent considers several attributes, based on which they try to learn the existing opponent modeling techniques.</a>
<a href="#9" id="9">These models are mostly based on the first assumption, that is, these models make strong assumptions on the bidding strategy of the opponent.</a>
<a href="#10" id="10">With this assumption, the hypothesis space of possible utility functions will be limited to the possible orders of negotiation issue weights.</a>
<a href="#11" id="11">Consequently, the ability of the model to extract the most information possible from the training bids it receives is highly essential.</a>
